function hari(angka) {
  if (angka == 1) {
    console.log("Senin");
  } else if (angka == 2) {
    console.log("Selasa");
  } else if (angka == 3) {
    console.log("Rabu");
  } else if (angka == 4) {
    console.log("Kamis");
  } else if (angka == 5) {
    console.log("Jumat");
  } else if (angka == 6) {
    console.log("sabtu");
  } else if (angka == 7) {
    console.log("Minggu");
  } else console.log("Jumlah Hari cuma ada 7 gan !");
}

hari(4);
